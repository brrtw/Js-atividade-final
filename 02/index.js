let numero1 = parseFloat(prompt("Digite o primeiro numero:"));
let numero2 = parseFloat(prompt("Digite o segundo numero:"));

let soma = numero1 + numero2;
let subtracao = numero1 - numero2;
let multiplicacao = numero1 * numero2;
let divisao = numero1 / numero2;

console.log(`A soma de ${numero1} e ${numero2} e igual a ${soma}.`);
console.log(`A subtracao de ${numero1} por ${numero2} e igual a ${subtracao}.`);
console.log(`A multiplicacao de ${numero1} por ${numero2} e igual a ${multiplicacao}.`);
console.log(`A divisao de ${numero1} por ${numero2} e igual a ${divisao}.`);
